

/** \brief solicita un número al usuario y devuelve el resultado
 *
 * \param mensaje: es el mensaje que pide ingresar datos
 * \return devuelve el numero ingresado
 */
int getInt (char mensaje[]);

/** \brief solicita un número flotante al usuario y devuelve el resultado
 *
 * \param mensaje: es el mensaje que pide ingresar datos
 * \return devuelve el numero flotante ingresado
 */
float getFloat(char mensaje[]);

/** \brief solicita una letra al usuario y devuelve el resultado
 *
 * \param mensaje: es el mensaje que pide ingresar datos
 * \return devuelve la letra ingresada
 */
char getChar(char mensaje[]);
